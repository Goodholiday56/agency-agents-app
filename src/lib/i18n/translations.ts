const locales: Record<string, LocaleMap> = {
  en,
  "zh-CN": zhCN,
  "zh-TW": zhTWFull,
  ja,
  ko,
  es,
  fr,
  de,
  pt,
  ru,
};

export const SUPPORTED_LOCALES = Object.keys(locales);

export function translate(lang: string, key: string, replacements?: Record<string, string | number>): string {
  let map = locales[lang];
  if (!map) {
    const prefix = lang.split("-")[0];
    map = locales[prefix];
  }
  const raw = map?.[key] ?? en[key] ?? key;
  if (!replacements) return raw;
  return raw.replace(/\{(\w+)\}/g, (_m, token) => String(replacements[token] ?? `{${token}}`));
}
